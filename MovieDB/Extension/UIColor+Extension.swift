import SwiftUI

extension UIColor {
    
    //common background color
    static let bg50: UIColor = UIColor(Color("bg-50", bundle: .main))
    static let bg100: UIColor = UIColor(Color("bg-100", bundle: .main))
    
    //primary color
    static let primary50: UIColor = UIColor(Color("primary-50", bundle: .main))
    static let primary500: UIColor = UIColor(Color("primary-500", bundle: .main))
}
